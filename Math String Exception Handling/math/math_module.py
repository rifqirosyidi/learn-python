import math


print("Number : 3.7 ")
print("ceil(3.7)  : ", math.ceil(3.7))
print("floor(3.7) : ", math.floor(3.7))
print("fabs(3.7)  : ", math.fabs(-3.7))

# Factorial 1 * 2 * 3 * 4
print("factorial(4) : ", math.factorial(4))

# Remainder of division
print("fmod(10, 3) : ", math.fmod(10, 3))

# Receive Float and return an integer
print("trunc(4.2) : ", math.trunc(4.2))

# Return x^y
print("pow(3,3) : ", math.pow(3, 3))

# Return Square Root
print("sqrt(144) : ", math.sqrt(144))

# Special Values
print("math.e : ", math.e)
print("math.pi : ", math.pi)

print("log(100, 10) : ", math.log(100, 10))

